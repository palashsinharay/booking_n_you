// JavaScript Document
$(document).ready(function() {
		$("#details_tabing ul li").click(function() {
		
		   $( '#details_tabing ul li').each(function(){
			 $('#details_tabing ul li ').removeClass();
			 currentId = $(this).attr('id');
			 $('#div'+currentId).hide();
		   }); 
		   
		   $(this).addClass('select');
		   currentId = $(this).attr('id');
		   $('#div'+currentId).show();
		});
		
		
  });